/*
Normalization: It is process of organizing the data in to tables such that there is minimum dependency and data integrity and remove duplicates
Steps:
1st Normal form(1NF): Removes the repeating groups/non-atomic attribtes
2nd Normal form(2NF): Removes the Partial Dependency(Like 1 variable depended on 1 candidate key and that is depended on primary key)
3rd Normal form(3NF): Removes the Transitive dependency(which is full dependency)
Boyce-codd Normal form(BCNF): It ensures every determinent is a candidate key and eliminates most redundancy
->BCNF is a stronger version of the 3NF
